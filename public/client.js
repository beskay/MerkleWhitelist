async function main() {
  if (window.ethereum) {
    handleEthereum();
  } else {
    window.addEventListener("ethereum#initialized", handleEthereum, {
      once: true,
    });

    // If the event is not dispatched by the end of the timeout,
    // the user probably doesn't have MetaMask installed.
    setTimeout(handleEthereum, 3000); // 3 seconds
  }

  async function handleEthereum() {
    const { ethereum } = window;

    //get connectbutton
    connectButton = document.getElementById("connectButton");

    //get function button
    funcBtn = document.getElementById("btn-sc");

    if (ethereum) {
      console.log("Ethereum successfully detected!");
    } else {
      //no metamask detected, update DOM
      console.log("Please install MetaMask!");

      connectButton = document.getElementById("connectButton");
      connectButton.href = "https://metamask.io/download.html";
      connectButton.innerHTML = "Install MetaMask";
      connectButton.target = "_blank";
    }

    connectButton.addEventListener("click", async () => {
      provider = new ethers.providers.Web3Provider(ethereum);

      // Request account access if needed
      try {
        await provider.send("eth_requestAccounts", []);
      } catch (err) {
        console.log("Connection rejected");
        return 0;
      }

      //initialize signer and account address
      signer = provider.getSigner();
      account = await signer.getAddress();
      console.log("Account:", account);

      //initialize contract
      contract = new ethers.Contract(contractAddress, abi, signer);

      //Add event listener NFT mint
      contract.on("Transfer", function (from, to, tokenId) {
        //only show if transferevent is for own account
        if (to == account) {
          console.log(`Minted Token ID ${tokenId} to ${to}`);
        }
      });
    });

    funcBtn.addEventListener("click", async () => {
      console.log("Button clicked");

      const response = await fetch(`/retData/${account}`);
      const proof = await response.json();

      // Call function, mint random token id (0-99) if account is whitelisted
      await contract.redeem(account, Math.floor(Math.random() * 100), proof);
    });
  }
}

window.addEventListener("load", main);
